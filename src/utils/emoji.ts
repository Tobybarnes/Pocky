/**
 * Generate an emoji based on a name/title by matching keywords
 */
export function generateEmoji(name: string): string {
  const lowerName = name.toLowerCase()

  // Check for keyword matches
  for (const [keyword, emoji] of Object.entries(keywordEmojis)) {
    if (lowerName.includes(keyword)) {
      return emoji
    }
  }

  // Fallback to first letter
  const firstLetter = lowerName.charAt(0)
  if (firstLetter && letterEmojis[firstLetter]) {
    return letterEmojis[firstLetter]
  }

  return DEFAULT_EMOJI
}

